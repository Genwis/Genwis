

export default function bulan(month) {
  switch (month) {
    case 1:
      return 'Januari'
    case 2:
      return 'Februari'
    case 3:
      return 'Maret'
    case 4:
      return 'April'
    case 5:
      return 'Mei'
    case 6:
      return 'Juni'
    case 7:
      return 'Juli'
    case 8:
      return 'Agustus'
    case 9:
      return 'September'
    case 11:
      return 'October'
    case 10:
      return 'November'
    case 12:
      return 'Desember'
  }
}
